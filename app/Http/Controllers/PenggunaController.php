    {
        $role = ModelRole::all();
        $pengguna = User::orderBy('last_Login_at', 'desc')->get();
        foreach ($pengguna as $user) {
            $user->lastLogin = $user->last_login_at
                ? Carbon::parse($user->last_login_at)->diffForHumans()
                : null;
        }

        return view('pengguna.index',[
            'pengguna' => $pengguna,
            'role' =>  $role,
        ]);
    }

